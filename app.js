function initNavigation() {
    const mobileMenuToggle = document.querySelector('.mobile-menu-toggle');
    const mobileMenu = document.querySelector('.nav-links');

    if (!mobileMenuToggle || !mobileMenu) return;

    let isMenuOpen = false;

    function openMenu() {
        isMenuOpen = true;
        mobileMenu.classList.add('active');
        mobileMenuToggle.classList.add('active');
        mobileMenuToggle.setAttribute('aria-expanded', 'true');
        mobileMenu.setAttribute('aria-hidden', 'false');

        // Accessibility: focus first link
        const firstLink = mobileMenu.querySelector('a');
        if (firstLink) firstLink.focus();
    }

    function closeMenu() {
        isMenuOpen = false;
        mobileMenu.classList.remove('active');
        mobileMenuToggle.classList.remove('active');
        mobileMenuToggle.setAttribute('aria-expanded', 'false');
        mobileMenu.setAttribute('aria-hidden', 'true');

        // Accessibility: return focus to toggle button
        mobileMenuToggle.focus();
    }

    // Toggle menu
    mobileMenuToggle.addEventListener('click', function () {
        if (isMenuOpen) closeMenu();
        else openMenu();
    });

    // Close with ESC key
    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape' && isMenuOpen) {
            closeMenu();
        }
    });

    // Smooth scrolling
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });

                if (mobileMenu.classList.contains('active')) closeMenu();
            }
        });
    });
}

function initLanguageDropdown() {
    const languageToggle = document.getElementById('language-toggle');
    const languageMenu = document.getElementById('language-menu');

    if (!languageToggle || !languageMenu) return;

    let isLanguageMenuOpen = false;

    const setMenuVisible = (visible) => {
        languageMenu.style.display = visible ? 'block' : 'none';
    };

    setMenuVisible(false);

    const openMenu = () => {
        isLanguageMenuOpen = true;
        languageMenu.classList.add('active');
        setMenuVisible(true);
        languageToggle.setAttribute('aria-expanded', 'true');
    };

    const closeMenu = () => {
        isLanguageMenuOpen = false;
        languageMenu.classList.remove('active');
        setMenuVisible(false);
        languageToggle.setAttribute('aria-expanded', 'false');
        languageToggle.blur();
    };

    languageToggle.addEventListener('click', function (e) {
        e.preventDefault();
        e.stopPropagation();
        if (isLanguageMenuOpen) closeMenu();
        else openMenu();
    });

    languageMenu.addEventListener('click', function (e) {
        const option = e.target.closest('.language-option');
        if (!option) return;
        e.preventDefault();
        const selectedLang = option.getAttribute('data-lang');
        const langNameEl = option.querySelector('.lang-name');
        const langName = langNameEl ? langNameEl.textContent : 'language';

        if (typeof window.setLanguage === 'function') {
            window.setLanguage(selectedLang);
        } else {
            localStorage.setItem('selectedLanguage', selectedLang);
        }

        const prefix = getTranslation('Language changed to', currentLanguage || selectedLang);
        showLanguageNotification(`${prefix} ${langName}`);
        closeMenu();
    });

    document.addEventListener('click', function (e) {
        if (!isLanguageMenuOpen) return;
        if (languageMenu.contains(e.target) || languageToggle.contains(e.target)) return;
        closeMenu();
    });

    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape' && isLanguageMenuOpen) {
            closeMenu();
        }
    });

    const savedLang = localStorage.getItem('selectedLanguage');
    if (savedLang) {
        const selectedOption = languageMenu.querySelector(`.language-option[data-lang="${savedLang}"]`);
        if (selectedOption) {
            selectedOption.style.background = 'rgba(108, 92, 231, 0.3)';
        }
    }
}

