let currentLanguage = 'en';
const i18nState = {
    textNodeKey: new WeakMap()
};

function getTranslation(key, lang) {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
    return dict[key] || TRANSLATIONS.en[key] || key;
}

window.getTranslation = getTranslation;

function translateAttributes(dict, fallback) {
    const selector = '[placeholder], [title], [aria-label], [alt], [value]';
    const attrs = ['placeholder', 'title', 'aria-label', 'alt', 'value'];

    document.querySelectorAll(selector).forEach((el) => {
        attrs.forEach((attr) => {
            if (!el.hasAttribute(attr)) return;
            const raw = el.getAttribute(attr);
            if (!raw || !raw.trim()) return;

            const dataKey = `data-i18n-${attr}`;
            if (!el.hasAttribute(dataKey)) {
                el.setAttribute(dataKey, raw.trim());
            }

            const key = el.getAttribute(dataKey);
            const translated = dict[key] || fallback[key];
            if (translated && translated !== el.getAttribute(attr)) {
                el.setAttribute(attr, translated);
            }
        });
    });
}

function translateTextNodes(dict, fallback) {
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
        acceptNode: (node) => {
            if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
            const parent = node.parentElement;
            if (!parent) return NodeFilter.FILTER_REJECT;
            const tag = parent.tagName;
            if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT') {
                return NodeFilter.FILTER_REJECT;
            }
            return NodeFilter.FILTER_ACCEPT;
        }
    });

    let node;
    while ((node = walker.nextNode())) {
        const raw = node.nodeValue;
        const trimmed = raw.trim().replace(/\s+/g, ' ');
        if (!trimmed) continue;

        let key = i18nState.textNodeKey.get(node);
        if (!key) {
            key = trimmed;
            i18nState.textNodeKey.set(node, key);
        }

        const translated = dict[key] || fallback[key];
        if (translated && translated !== trimmed) {
            node.nodeValue = raw.replace(raw.trim(), translated);
        }
    }
}

function applyTranslations(lang) {
    currentLanguage = lang;
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
    const fallback = TRANSLATIONS.en;
    document.documentElement.setAttribute('lang', lang);
    translateAttributes(dict, fallback);
    translateTextNodes(dict, fallback);
}

function initI18n() {
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    applyTranslations(savedLang);
}

window.setLanguage = function (lang) {
    localStorage.setItem('selectedLanguage', lang);
    applyTranslations(lang);
};

